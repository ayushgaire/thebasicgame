export type Word = (typeof WORDS)[number];

export function randomWord(): Word {
  return WORDS[Math.floor(Math.random() * WORDS.length)];
}

export function isValidWord(w: string): boolean {
  return (WORDS as readonly string[]).includes(w.toUpperCase());
}

export type LetterState = 'correct' | 'present' | 'absent';

/**
 * Score a guess against a target word. Handles double letters correctly.
 */
export function scoreGuess(guess: string, target: string): LetterState[] {
  const g = guess.toUpperCase().split('');
  const t = target.toUpperCase().split('');
  const result: LetterState[] = Array(5).fill('absent');
  const taken = Array(5).fill(false);

  // First pass: correct positions
  for (let i = 0; i < 5; i++) {
    if (g[i] === t[i]) {
      result[i] = 'correct';
      taken[i] = true;
    }
  }
  // Second pass: present but wrong position
  for (let i = 0; i < 5; i++) {
    if (result[i] === 'correct') continue;
    for (let j = 0; j < 5; j++) {
      if (!taken[j] && g[i] === t[j]) {
        result[i] = 'present';
        taken[j] = true;
        break;
      }
    }
  }
  return result;
}
